// ========================================
// üéØ CLASSE PRINCIPALE KANBAN
// ========================================
class ProjectKanban {
    constructor() {
        this.projectId = this.getProjectIdFromUrl();
        this.project = null;
        this.tasks = [];
        this.currentTaskId = null;
    }

    // ========================================
    // üöÄ INITIALISATION
    // ========================================
    async init() {
        console.log('üéØ Initialisation du Kanban pour le projet:', this.projectId);
        await this.loadProject();
        this.loadTasks();
        this.renderTasks();
        this.setupEventListeners();
        this.setupDragAndDrop();
    }

    // ========================================
    // üîó R√âCUP√âRER L'ID DU PROJET DEPUIS L'URL
    // ========================================
    getProjectIdFromUrl() {
        const params = new URLSearchParams(window.location.search);
        const id = params.get('id');
        console.log('üìç ID du projet depuis URL:', id);
        return id;
    }

    // ========================================
    // üìÇ CHARGER LE PROJET
    // ========================================
    async loadProject() {
        console.log('üìÇ Chargement du projet...');
        try {
            const password = AuthManager.getCurrentPassword();
            if (!password) {
                throw new Error('Mot de passe non trouv√©');
            }

            const userData = await StorageManager.loadData(password);
            const projects = userData.projects || [];
            this.project = projects.find(p => p.id === this.projectId);

            if (!this.project) {
                console.error('‚ùå Projet introuvable');
                alert('Projet introuvable');
                window.location.href = 'dashboard.html';
                return;
            }

            console.log('‚úÖ Projet charg√©:', this.project);
            this.displayProjectInfo();
        } catch (error) {
            console.error('‚ùå Erreur chargement:', error);
            alert('Erreur lors du chargement du projet');
            window.location.href = 'dashboard.html';
        }
    }

    // ========================================
    // üìä AFFICHER LES INFOS DU PROJET
    // ========================================
    displayProjectInfo() {
        const nameEl = document.getElementById('projectName');
        if (nameEl && this.project) {
            nameEl.textContent = this.project.title;
        }
        console.log('‚úÖ Infos du projet affich√©es');
    }

    // ========================================
    // üìã CHARGER LES T√ÇCHES
    // ========================================
    loadTasks() {
        console.log('üìã Chargement des t√¢ches...');
        const stored = localStorage.getItem(`tasks_${this.projectId}`);
        this.tasks = stored ? JSON.parse(stored) : [];
        console.log('‚úÖ T√¢ches charg√©es:', this.tasks.length);
        this.updateStats();
    }

    // ========================================
    // üéß CONFIGURATION DES √âV√âNEMENTS
    // ========================================
    setupEventListeners() {
        console.log('üéß Configuration des √©v√©nements...');

        // Boutons "Ajouter une t√¢che"
        document.querySelectorAll('.add-task-btn').forEach(btn => {
            btn.addEventListener('click', (e) => {
                console.log('üÜï Clic sur bouton nouvelle t√¢che');
                const column = e.target.closest('.kanban-column');
                const status = column.dataset.status;
                console.log('üìç Status de la colonne:', status);
                this.showTaskModal(status);
            });
        });

        // Formulaire de t√¢che
        const taskForm = document.getElementById('taskForm');
        if (taskForm) {
            taskForm.addEventListener('submit', (e) => this.saveTask(e));
        }

        // Fermeture du modal
        const closeBtn = document.querySelector('.close');
        if (closeBtn) {
            closeBtn.addEventListener('click', () => this.closeTaskModal());
        }

        // Fermeture si clic en dehors
        window.addEventListener('click', (e) => {
            const modal = document.getElementById('taskModal');
            if (e.target === modal) {
                this.closeTaskModal();
            }
        });

        console.log('‚úÖ √âv√©nements configur√©s');
    }

    // ========================================
    // üéØ DRAG & DROP - CONFIGURATION
    // ========================================
    setupDragAndDrop() {
        console.log('üéØ Configuration Drag & Drop...');

        const containers = document.querySelectorAll('.cards-container');
        
        containers.forEach(container => {
            // Autoriser le survol
            container.addEventListener('dragover', (e) => {
                e.preventDefault();
                e.stopPropagation();
                container.classList.add('drag-over');
            });

            // Retirer la classe au d√©part
            container.addEventListener('dragleave', (e) => {
                e.preventDefault();
                e.stopPropagation();
                container.classList.remove('drag-over');
            });

            // G√©rer le drop
            container.addEventListener('drop', (e) => {
                e.preventDefault();
                e.stopPropagation();
                container.classList.remove('drag-over');
                
                const taskId = e.dataTransfer.getData('text/plain');
                const newStatus = container.id.replace('-cards', '');
                
                console.log('üì¶ Drop d√©tect√© - TaskID:', taskId, 'Nouveau status:', newStatus);
                
                if (taskId) {
                    this.moveTask(taskId, newStatus);
                }
            });
        });

        this.makeDraggable();
        
        console.log('‚úÖ Drag & Drop configur√©');
    }

    // ========================================
    // üéØ RENDRE LES CARTES DRAGGABLES
    // ========================================
    makeDraggable() {
        const cards = document.querySelectorAll('.task-card');
        console.log('üé¥ Configuration de', cards.length, 'cartes draggables');
        
        cards.forEach(card => {
            card.setAttribute('draggable', 'true');
            
            // D√©but du drag
            card.addEventListener('dragstart', (e) => {
                const taskId = card.dataset.taskId;
                e.dataTransfer.effectAllowed = 'move';
                e.dataTransfer.setData('text/plain', taskId);
                card.classList.add('dragging');
                console.log('üéØ Drag start - TaskID:', taskId);
            });

            // Fin du drag
            card.addEventListener('dragend', (e) => {
                card.classList.remove('dragging');
                document.querySelectorAll('.drag-over').forEach(el => {
                    el.classList.remove('drag-over');
                });
                console.log('‚úÖ Drag end');
            });
        });
    }

    // ========================================
    // üîÑ D√âPLACER UNE T√ÇCHE
    // ========================================
    moveTask(taskId, newStatus) {
        console.log('üîÑ moveTask appel√© - TaskID:', taskId, 'Nouveau status:', newStatus);
        
        const task = this.tasks.find(t => t.id === taskId);
        
        if (!task) {
            console.error('‚ùå T√¢che introuvable:', taskId);
            return;
        }

        const oldStatus = task.status;
        
        if (oldStatus === newStatus) {
            console.log('‚ÑπÔ∏è M√™me statut, pas de changement');
            return;
        }

        // Mettre √† jour la t√¢che
        task.status = newStatus;
        task.updatedAt = new Date().toISOString();
        
        console.log('‚úÖ T√¢che mise √† jour:', task.name, oldStatus, '‚Üí', newStatus);
        
        // Sauvegarder
        localStorage.setItem(`tasks_${this.projectId}`, JSON.stringify(this.tasks));
        
        // Re-rendre l'interface
        this.renderTasks();
        this.updateStats();
        
        console.log('‚úÖ Interface mise √† jour');
    }

    // ========================================
    // üìù AFFICHER LE MODAL DE T√ÇCHE
    // ========================================
    showTaskModal(status = 'todo', taskId = null) {
        console.log('üìù Ouverture modal - Status:', status, 'TaskID:', taskId);
        
        const modal = document.getElementById('taskModal');
        const form = document.getElementById('taskForm');
        const title = document.getElementById('modalTitle');

        this.currentTaskId = taskId;

        if (taskId) {
            // Mode √©dition
            const task = this.tasks.find(t => t.id === taskId);
            if (task) {
                title.textContent = 'Modifier la t√¢che';
                document.getElementById('taskName').value = task.name;
                document.getElementById('taskDescription').value = task.description || '';
                document.getElementById('taskPriority').value = task.priority;
                document.getElementById('taskDueDate').value = task.dueDate || '';
                document.getElementById('taskStatus').value = task.status;
                document.getElementById('taskAssignee').value = task.assignee || '';
                console.log('üìù Mode √©dition - T√¢che charg√©e:', task.name);
            }
        } else {
            // Mode cr√©ation
            title.textContent = 'Nouvelle t√¢che';
            form.reset();
            document.getElementById('taskStatus').value = status;
            console.log('üìù Mode cr√©ation - Status par d√©faut:', status);
        }

        modal.classList.add('active');
    }

    // ========================================
    // ‚ùå FERMER LE MODAL
    // ========================================
    closeTaskModal() {
        console.log('‚ùå Fermeture du modal');
        const modal = document.getElementById('taskModal');
        modal.classList.remove('active');
        this.currentTaskId = null;
        document.getElementById('taskForm').reset();
    }

    // ========================================
    // üíæ SAUVEGARDER UNE T√ÇCHE
    // ========================================
    saveTask(e) {
        e.preventDefault();
        console.log('üíæ Sauvegarde de la t√¢che...');

        const formData = {
            name: document.getElementById('taskName').value.trim(),
            description: document.getElementById('taskDescription').value.trim(),
            priority: document.getElementById('taskPriority').value,
            dueDate: document.getElementById('taskDueDate').value,
            status: document.getElementById('taskStatus').value,
            assignee: document.getElementById('taskAssignee').value.trim()
        };

        console.log('üìã Donn√©es du formulaire:', formData);

        if (this.currentTaskId) {
            // Mise √† jour
            const task = this.tasks.find(t => t.id === this.currentTaskId);
            if (task) {
                Object.assign(task, formData);
                task.updatedAt = new Date().toISOString();
                console.log('‚úÖ T√¢che mise √† jour:', task.name);
            }
        } else {
            // Cr√©ation
            const newTask = {
                id: 'task_' + Date.now() + '_' + Math.random().toString(36).substr(2, 9),
                ...formData,
                createdAt: new Date().toISOString(),
                updatedAt: new Date().toISOString()
            };
            this.tasks.push(newTask);
            console.log('‚úÖ Nouvelle t√¢che cr√©√©e:', newTask.name, 'ID:', newTask.id);
        }

        // Sauvegarder
        localStorage.setItem(`tasks_${this.projectId}`, JSON.stringify(this.tasks));
        console.log('üíæ T√¢ches sauvegard√©es dans localStorage');

        // Mettre √† jour l'affichage
        this.renderTasks();
        this.updateStats();
        this.closeTaskModal();

        console.log('‚úÖ Sauvegarde termin√©e');
    }

    // ========================================
    // üé® AFFICHER LES T√ÇCHES
    // ========================================
    renderTasks() {
        console.log('üé® Rendu des t√¢ches...');
        
        // Vider les colonnes
        ['todo', 'in-progress', 'done'].forEach(status => {
            const container = document.getElementById(`${status}-cards`);
            if (container) {
                container.innerHTML = '';
            }
        });

        // Ajouter les t√¢ches
        this.tasks.forEach(task => {
            const card = this.createTaskCard(task);
            const container = document.getElementById(`${task.status}-cards`);
            if (container) {
                container.appendChild(card);
            }
        });

        // Reconfigurer le drag & drop
        this.makeDraggable();

        // Mettre √† jour les compteurs
        this.updateTaskCounts();
        
        console.log('‚úÖ T√¢ches rendues:', this.tasks.length);
    }

    // ========================================
    // üé¥ CR√âER UNE CARTE DE T√ÇCHE
    // ========================================
    createTaskCard(task) {
        const card = document.createElement('div');
        card.className = 'task-card';
        card.dataset.priority = task.priority;
        card.dataset.taskId = task.id;

        const priorityLabels = {
            low: 'üü¢ Basse',
            medium: 'üü° Moyenne',
            high: 'üî¥ Haute'
        };

        // Gestion de la date d'√©ch√©ance
        let dateClass = '';
        let dateText = '';
        if (task.dueDate) {
            const dueDate = new Date(task.dueDate);
            const today = new Date();
            today.setHours(0, 0, 0, 0);
            dueDate.setHours(0, 0, 0, 0);

            const diffTime = dueDate - today;
            const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));

            if (diffDays < 0) {
                dateClass = 'overdue';
            } else if (diffDays <= 2) {
                // V√©rifier si c'est un jour ouvr√©
                const dayOfWeek = dueDate.getDay();
                if (dayOfWeek !== 0 && dayOfWeek !== 6) {
                    dateClass = 'due-soon';
                }
            }

            dateText = dueDate.toLocaleDateString('fr-FR');
        }

        card.innerHTML = `
            <div class="task-header">
                <h4 class="task-title">${task.name}</h4>
                <div class="task-actions">
                    <button class="task-btn" onclick="app.editTask('${task.id}')" title="Modifier">
                        ‚úèÔ∏è
                    </button>
                    <button class="task-btn" onclick="app.deleteTask('${task.id}')" title="Supprimer">
                        üóëÔ∏è
                    </button>
                </div>
            </div>
            ${task.description ? `<p class="task-description">${task.description}</p>` : ''}
            <div class="task-footer">
                <span class="task-priority">${priorityLabels[task.priority]}</span>
                ${dateText ? `<span class="task-date ${dateClass}">üìÖ ${dateText}</span>` : ''}
            </div>
        `;

        return card;
    }

    // ========================================
    // ‚úèÔ∏è √âDITER UNE T√ÇCHE
    // ========================================
    editTask(taskId) {
        console.log('‚úèÔ∏è √âdition de la t√¢che:', taskId);
        const task = this.tasks.find(t => t.id === taskId);
        if (task) {
            this.showTaskModal(task.status, taskId);
        }
    }

    // ========================================
    // üóëÔ∏è SUPPRIMER UNE T√ÇCHE
    // ========================================
    deleteTask(taskId) {
        console.log('üóëÔ∏è Demande de suppression:', taskId);
        const task = this.tasks.find(t => t.id === taskId);
        
        if (task && confirm(`Voulez-vous vraiment supprimer la t√¢che "${task.name}" ?`)) {
            this.tasks = this.tasks.filter(t => t.id !== taskId);
            localStorage.setItem(`tasks_${this.projectId}`, JSON.stringify(this.tasks));
            this.renderTasks();
            this.updateStats();
            console.log('‚úÖ T√¢che supprim√©e');
        } else {
            console.log('‚ùå Suppression annul√©e');
        }
    }

    // ========================================
    // üî¢ METTRE √Ä JOUR LES COMPTEURS
    // ========================================
    updateTaskCounts() {
        const counts = {
            'todo': 0,
            'in-progress': 0,
            'done': 0
        };

        this.tasks.forEach(task => {
            if (counts.hasOwnProperty(task.status)) {
                counts[task.status]++;
            }
        });

        Object.keys(counts).forEach(status => {
            const countEl = document.getElementById(`${status}-count`);
            if (countEl) {
                countEl.textContent = counts[status];
            }
        });

        console.log('üî¢ Compteurs mis √† jour:', counts);
    }

    // ========================================
    // üìä METTRE √Ä JOUR LES STATISTIQUES
    // ========================================
    updateStats() {
        const total = this.tasks.length;
        const done = this.tasks.filter(t => t.status === 'done').length;
        const progress = total > 0 ? Math.round((done / total) * 100) : 0;

        const totalEl = document.getElementById('totalTasks');
        const doneEl = document.getElementById('doneTasks');
        const progressEl = document.getElementById('progressPercent');

        if (totalEl) totalEl.textContent = total;
        if (doneEl) doneEl.textContent = done;
        if (progressEl) progressEl.textContent = `${progress}%`;

        console.log('üìä Stats mises √† jour - Total:', total, 'Termin√©es:', done, 'Progr√®s:', progress + '%');
    }

    // ========================================
    // üóëÔ∏è SUPPRIMER LE PROJET
    // ========================================
    async deleteProject() {
        console.log('üóëÔ∏è Demande de suppression du projet');
        
        if (confirm(`Voulez-vous vraiment supprimer le projet "${this.project.name}" et toutes ses t√¢ches ?`)) {
            try {
                const password = AuthManager.getCurrentPassword();
                const userData = await StorageManager.loadData(password);
                
                // Supprimer le projet
                userData.projects = userData.projects.filter(p => p.id !== this.projectId);
                
                // Sauvegarder
                await StorageManager.saveData(userData, password);
                
                // Supprimer les t√¢ches
                localStorage.removeItem(`tasks_${this.projectId}`);

                console.log('‚úÖ Projet supprim√©');

                // Retour au dashboard
                window.location.href = 'dashboard.html';
            } catch (error) {
                console.error('‚ùå Erreur suppression:', error);
                alert('Erreur lors de la suppression du projet');
            }
        } else {
            console.log('‚ùå Suppression annul√©e');
        }
    }
}

// ========================================
// üöÄ INITIALISATION AU CHARGEMENT
// ========================================
document.addEventListener('DOMContentLoaded', () => {
    console.log('üöÄ DOM charg√©, cr√©ation de l\'instance ProjectKanban...');
    window.app = new ProjectKanban();
    window.app.init();
});
